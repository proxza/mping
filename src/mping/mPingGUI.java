        jButton2.setEnabled(true);
        
        String host = jTextField1.getText(); // Получаем хост
        
        new Thread(new Runnable() {
            public void run() {
                while (true) { //бесконечно крутим
                    try {
                        if (startPress) {
                            throw new InterruptedException();
                        }
                        timer = Integer.parseInt(jTextField2.getText());
                        getServerFromIP(host);
                        Thread.sleep(timer); // timer
                    } catch (InterruptedException e) {
                        Thread.currentThread().interrupt();
                        return;
                        //e.printStackTrace();
                    }
                }
            }
        }).start();

