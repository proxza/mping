    // Главный метод
    public void getServerFromIP(String ip) {
        String result = "";
        try {

            //String ip = jTextField1.getText();
            InetAddress inet = InetAddress.getByName(ip);
            boolean isReachable = inet.isReachable(5000);

            if (isReachable) {
                countTrue += 1;
                result = "Ответ от хоста: " + ip + " пришел [" + inet.getHostAddress() + "]" + "\n";
                jTextArea1.append(result);

                String lbl = "Количество успешных пакетов: " + countTrue;
                jLabel1.setText(lbl);
            } else {
                countFalse += 1;
                result = "Ответ от хоста: " + ip + " НЕ пришел [" + inet.getHostAddress() + "]" + "\n";
                jTextArea1.append(result);

                String lbl = "Количество потерянных пакетов: " + countFalse;
                jLabel2.setText(lbl);
            }

            // Вывод процентов
            if (countTrue > 0 && countFalse > 0) {
                int percent = (countFalse * 100) / countTrue;
                jLabel5.setText("Процент потерь: " + percent + "%");
            }
            
            // Каретка будет переходить на новую строку (в конце скролла)
            jTextArea1.setCaretPosition(jTextArea1.getDocument().getLength());

        } catch (UnknownHostException ex) {
            result = "Сервер не найден!\n";
            jTextArea1.append(result);
            startPress = true;
        } catch (IOException ex) {
            ex.printStackTrace();
        }

    }

